package com.example.SpringBootJPAMultiDataSource.entity1;

import javax.persistence.*;

@Entity
@Table(name = "publishers", schema = "publisher", catalog = "")
@NamedStoredProcedureQuery(name = "sp_GetPublisher",procedureName = "sp_GetPublisher",resultClasses = PublishersEntity.class)
public class PublishersEntity {
    private long id;
    private String name;

    @Id
    @Column(name = "ID")
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    @Basic
    @Column(name = "NAME")
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        PublishersEntity that = (PublishersEntity) o;

        if (id != that.id) return false;
        if (name != null ? !name.equals(that.name) : that.name != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = (int) (id ^ (id >>> 32));
        result = 31 * result + (name != null ? name.hashCode() : 0);
        return result;
    }
}
